package com.syntax.class01;

public class PrimitiveDataTypes {
	
	public static void main (String [] args)
	{
		/* * Primitive Data Types
		 * 
		 * byte
		 * short
		 * int
		 * long 
		 * float
		 * double
		 * char
		 * boolean
		 */
		
		// Whole numbers data types
	
		byte num = 10;
		
		short num1 = 100;
				
		int num2 = 1000;
		
		// l or L
		long num3 = 1000009448811143234L;
		
		//Decimal Number Data Types
		
		// f or F
		float num4 = 12.9F;
		
		double num5 = 0.888;
		
		// Non number data types
		
		char add = '+';
		char letter = 'a';
		
		// true or false (yes or no)
		
		// do not put Yes or No for value
		boolean hungry = true;
		
		boolean thirsty = false;
		
		
		
		
		
		
		
		
	}

}
