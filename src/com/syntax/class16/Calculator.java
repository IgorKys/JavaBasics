package com.syntax.class16;

public class Calculator {

	// add, sub, div,mult

	void add(int a, int b) {
		System.out.println(a + b);
	}

	void sub(int a, int b) {
		System.out.println(a - b);
	}

	void div(int a, int b) {

		System.out.println(a / b);
	}

	void mult(int a, int b) {

		System.out.println(a * b);
	}

}
